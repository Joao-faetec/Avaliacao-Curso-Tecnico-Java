    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        
        try {
        // Tenta converter os valores dos campos
        double pesoValor = Double.parseDouble(peso.getText().replace(",", "."));
        double alturaValor = Double.parseDouble(altura.getText().replace(",", "."));

        // Verifica se os valores s√£o v√°lidos
        if (pesoValor <= 0 || alturaValor <= 0) {
            JOptionPane.showMessageDialog(this, "Peso ou altura inv√°lidos!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Calcula o IMC
        double imc = pesoValor / (alturaValor * alturaValor);
        String classificacao;

        // Define a classifica√ß√£o
        if (imc < 18.5) {
            classificacao = "Abaixo do peso";
        } else if (imc < 25) {
            classificacao = "Peso normal";
        } else if (imc < 30) {
            classificacao = "Sobrepeso";
        } else if (imc < 35) {
            classificacao = "Obesidade grau I";
        } else if (imc < 40) {
            classificacao = "Obesidade grau II";
        } else {
            classificacao = "Obesidade grau III";
        }

        // Exibe o resultado na JLabel
        resultado.setText(String.format("IMC: %.1f - %s", imc, classificacao));

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
        
    }//GEN-LAST:event_calcularActionPerformed

